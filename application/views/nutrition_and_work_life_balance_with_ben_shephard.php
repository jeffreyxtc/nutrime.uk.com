                           <p> <img src="<?php echo base_url()?>assets/images/ben-shephard-top.jpg" width="535" height="280"> </p>
                           <p>Despite having one of television’s most punishing schedules, Good Morning Britain's Ben Shephard found the time to chat with me about Nutrition and work life balance. Check out what Ben had to say about his daily diet and what are his 'Desert Island Dishes'.</p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>Do you ever cook at home?</p>
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>Not really.  Annie cooks.  She’s a lot better cook than me.  A lot quicker and it’s a lot tastier. Although she’s a lot messier!  If I am going to cook it’s usually very simple fresh veg and meat of some sort.</p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>So you do cook occasionally?
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>Yes occasionally.  But it has to be easy, and quick, no frills. Lots of steamed veg!!
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>How about your mum, was she a good cook and how has she influenced you?
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>She’s a great cook.  A brilliant improviser.  You know, open the fridge and create something really lovely.  And interestingly now my dad has retired he has become a fantastic cook.  He absolutely loves it – although his portions could be bigger!
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>How do you relax?
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>I try to run a couple of times a week; I play football with friends, a bit of golf here and there.  I swim and I like cycling but I don’t like all the paraphernalia that goes with cycling.  I’d rather just get the trainers on and go running.
                           <p>I think that one of the drivers for me keeping fit is it means I can manage on less sleep. I can get up, cope with that days programme and I’m relatively alert.  It’s one thing sleeping in batches of 2 hours and 5 hours to get 7 hours sleep, which is what I try to do, but it’s another thing doing that and then have to do live TV interviewing whoever may be on that day.
                           <p>My week usually involves me getting up at 3am four days a week for GMB and then early on Sunday for Goals on Sunday on Sky Sports.
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>So how do you tackle breakfast?</p>
                           <p>I usually take a bottle of water with me in the car on the way to work.  I’ll have a cup of tea when I reach work and then one every half hour or so. There’s a lot of tea involved about 7 or 8 cups of tea between 4am and 9am.
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>Caffeinated?</p>
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>It varies, but yes I’ve started drinking a bit more caffeine.
                              At 5.30am I’ll eat some porridge with water. I’ll also have some multi vitamins too to help get me ticking over.
                           </p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>Why not milk?</p>
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>I started not having milk when I did the Men’s Health six-pack challenge and I found that I just got the taste of it.  It was horrible initially but I know it’s less fattening, I quite like it now.</p>
                           <p>As soon as I come off air I’ll have some Activeman Oatein – which has been a great addition to my diet.</p>
                           <p>Working on a live show, your energy levels go and down so when you come off air you tend to have a really big slump.  It’s very easy at that point to grab there nearest whatever.</p>
                           <p>The big temptation when you come off air is to have some pastries or a fry up.</p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>Protein shakes can be good.  So that gets you through to lunch?</p>
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>Yes.  For lunch I like any kind of fish.  I love having fish and steamed veg.</p>
                           <p>As tempted as I am to have a burger and chips because that is what I really want I always feel so much better if I’ve had some fish with a nice salad or rice.  Also with lots of vegetables as well.</p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>You can never have too many vegetables.</p>
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>The only vegetable I won’t eat is Beetroot. </p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>Beetroot?
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>I’ve just got that fear from tasting it when I was a kid and we were forced to eat it at infant school.</p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>It is a difficult one I know, but try a little bit in a smoothie or soup to help hide the flavor.</p>
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>I try it every now and again because I understand how taste buds develop.  I used to hate mushrooms as a kid and now I love them.</p>
                           <p>I’ve got this big thing with the kids.  I don’t mind if they don’t eat something as long as they try it.   I don’t want them not to like something because I don’t like it.</p>
                           <p>I try not to eat too many carbs after Lunch.  I will literally stick to vegetables and meat.</p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>Do you eat all types of meat?</p>
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>Yes</p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>Meat is a good source of protein.  My question would be where is the meat from?  Where is it farmed?</p>
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>And how has it lived?</p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>Absolutely.</p>
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>We go to the local shop and get it from the local butcher.  My old university housemate is a lamb farmer so we get lamb from there, which is gorgeous.  Delicious!</p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>How about when you snack like nuts and seeds?</p>
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>I do love nuts and seeds. </p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>They are are full of your Omega 3’s and B Vitamins, all the stuff that you need for brainpower.</p>
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>My problem is I am very greedy.</p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>Portion control?</p>
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>Yes.  A handful of seeds its just not enough.</p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>How about drinks?  Do you have a lot of water?</p>
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>Yes but probably not as much as I should.</p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>A common issue I find.</p>
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>Yes I don’t really know if you can have as much as you are supposed to.
                              Every now and then I’ll have a coke.  I used to drink quite a lot of coke.
                           </p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>Full fat coke?</p>
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>Yes.  And Dr Pepper.</p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>Gosh I remember that!</p>
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>It was about the sugar hit really, but I stopped a few years back and literally cut it out completely.  Now it’s mostly water.  Sparkling because I find it more interesting than still water.
                           <p>I have a far less sweet tooth than I used to and I don’t tend to go for desserts anymore. Having said that I have been caught raiding the kids treats in the past to nick some jelly babies!
                              Alcohol wise hardly anything really compared to what I used to.  Seven to eight units a week.  The odd beer here and there.
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>Ok.  Just a few more questions….
                           <p>Name your “Desert island dishes”.  3 foods and a drink what would you take?
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>Ummm….right now I’d probably take asparagus.  I’ve got a real love for asparagus at the moment.
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>– Interesting, Asparagus is a rich source of folates, B-complex vitamins and antioxidants.
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>Yes.  And some beef.  I love it all.  Burgers, steaks.  Beef, beef, beef!  If I’m on a desert island there will be a load of fish anyway so I’d take beef.</p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>– Beef is a good source of protein, minerals (zinc, iron, selenium, phosphorous) and B Vitamins. And what else?
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>And Ice cream.  I don’t think there’s ever a bad time of year for ice cream.
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>– Ice cream is high in sugar, fat but does contain some calcium, potassium and some phosphorous.  To drink?
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>It would be between champagne and lager.  Probably lager.</p>
                           <p><strong>Now for the Quick-fire round:</strong></p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>Peanut butter or marmite?</p>
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>Peanut butter allover.</p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>Brown or white?</p>
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>Brown</p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>Indian or Chinese?</p>
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>Indian</p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>Run or yoga?</p>
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>Run</p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>Massage or sleep?</p>
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>Sleep.  I can’t bear massage.</p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>Steak or fish?</p>
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>Steak</p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>Vegetables or salad?</p>
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>Probably vegetables.</p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>Ketchup or salad cream?</p>
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>Ketchup</p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>Sweet or sour?</p>
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>Sweet</p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>Home-cooked or restaurant?</p>
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>Great question.  Probably restaurant.</p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>Custard or cream?</p>
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>Cream</p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>And finally your guilty pleasure?</p>
                           <p><span class="headerGreen">Ben Shephard</span></p>
                           <p>Sweaty feet crisps are my favorite.   Chemically flavoured.  Monsters munch, Wheatos, Quavers, Scampi Fires, Knick-Knacks, and Frazzles.  Anything with a strong flavor that isn’t real, you know?  Strong plastic flavours.  But I am only allowed them on a long car journey when Annie’s not with me.  I’m banned from having sweaty feet flavoured crisps when she’s in the car!</p>
                           <p><span class="headerGreen">Tess Scott</span></p>
                           <p>Ben is was a great pleasure to meet with you today and thank you for taking the time to share your daily diet and your desert island dishes with me.</p>
